export function getCareerById(id: string): Career | undefined {
  return careers.find((career) => career.id === id);
}

/**
 * Compute related career ids for each career based on domain and shared skills/tags.
 * Returns a map of careerId -> relatedCareerIds[] (max `maxRelated` per career).
 */
export function computeRelatedCareerIds(all: Career[], maxRelated = 4): Record<string, string[]> {
  const map: Record<string, string[]> = {};

  for (const a of all) {
    const scores: Record<string, number> = {};

    for (const b of all) {
      if (a.id === b.id) continue;
      let score = 0;
      if (a.domain && b.domain && a.domain === b.domain) score += 3;
      const aSkills = new Set([...(a.supportingSkills || []), ...(a.tags || [])]);
      const bSkills = new Set([...(b.supportingSkills || []), ...(b.tags || [])]);
      for (const s of aSkills) if (bSkills.has(s)) score += 1;
      if (a.coreSkill && b.coreSkill && a.coreSkill === b.coreSkill) score += 2;
      if (score > 0) scores[b.id] = score;
    }

    const related = Object.entries(scores)
      .sort(([, s1], [, s2]) => s2 - s1)
      .slice(0, maxRelated)
      .map(([id]) => id);

    map[a.id] = related;
  }

  return map;
}

