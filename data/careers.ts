export const AVAILABLE_BADGES = [
  "Future-Proof",
  "AI Native",
  "Emerging",
  "High Salary",
  "Remote Friendly",
  "Startup Friendly",
  "Beginner Friendly",
];

export function deriveBadges(career: Career): string[] {
  const badges: string[] = [];

  if (career.futureDemand === "Exploding" || career.futureDemand === "High Growth") {
    badges.push("Future-Proof");
  }

  if (career.aiRelationship === "AI-Created" || career.aiImpact === "transformative") {
    badges.push("AI Native");
  }

  if (career.futureDemand === "Exploding") {
    badges.push("Emerging");
  }

  // heuristic: high salary ranges starting with $12xk or $13xk
  if (career.salary && /\$1[2-9]0k|\$1[3-9]0k/.test(career.salary)) {
    badges.push("High Salary");
  }

  if (career.remotePotential === "High") {
    badges.push("Remote Friendly");
  }

  if ((career.tags || []).some((t) => /startup/i.test(t))) {
    badges.push("Startup Friendly");
  }

  if (career.difficulty === "low") {
    badges.push("Beginner Friendly");
  }

  return badges;
}

export interface CareerFacets {
  total: number;
  categories: Record<string, number>;
  aiImpact: Record<string, number>;
  difficulty: Record<string, number>;
  futureDemand: Record<string, number>;
  aiRelationship: Record<string, number>;
  remotePotential: Record<string, number>;
  badges: Record<string, number>;
}

export function getCareerFacets(careers: Career[]): CareerFacets {
  const facets: CareerFacets = {
    total: careers.length,
    categories: {},
    aiImpact: {},
    difficulty: {},
    futureDemand: {},
    aiRelationship: {},
    remotePotential: {},
    badges: {},
  };

  for (const career of careers) {
    facets.categories[career.category] = (facets.categories[career.category] ?? 0) + 1;
    if (career.aiImpact) facets.aiImpact[career.aiImpact] = (facets.aiImpact[career.aiImpact] ?? 0) + 1;
    if (career.difficulty) facets.difficulty[career.difficulty] = (facets.difficulty[career.difficulty] ?? 0) + 1;
    if (career.futureDemand) facets.futureDemand[career.futureDemand] = (facets.futureDemand[career.futureDemand] ?? 0) + 1;
    if (career.aiRelationship) facets.aiRelationship[career.aiRelationship] = (facets.aiRelationship[career.aiRelationship] ?? 0) + 1;
    if (career.remotePotential) facets.remotePotential[career.remotePotential] = (facets.remotePotential[career.remotePotential] ?? 0) + 1;
    for (const badge of deriveBadges(career)) {
      facets.badges[badge] = (facets.badges[badge] ?? 0) + 1;
    }
  }

  return facets;
}

